// Convert Celsius into Fahrenheit
var celsius=parseFloat(prompt('Enter a value for conversion :'));
var fahrenheit=(celsius*9/5)+32;
document.write('The conversion of celsius temperature into fahrenheit is '+fahrenheit+'<br>');

// Convert Fahrenheit into celsius
